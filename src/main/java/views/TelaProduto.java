    private void load() {
        ProdutoService service = SpringConfig.context.getBean(ProdutoService.class);
        
        if (!fieldSearchId.getText().equals("")) {
            entitySearch.setId(Integer.valueOf(fieldSearchId.getText()));
        }
        
        if (!fieldSearchNome.getText().equals("")) {
            entitySearch.setNome(fieldSearchNome.getText());
        }
        
        if (comboSearchAtivo.getSelectedIndex() != 0) {
            entitySearch.setAtivo(Arrays.asList(SimNaoType.values()).get(comboSearchAtivo.getSelectedIndex()-1));
        }
        
        populateTable(service.find(entitySearch));
        
        entitySearch = new Produto();
        ParamConfig config = new ParamConfig();
        
        System.out.println(config.getFuncionarioLogado().getNome());
    }
    
    private void populateTable(List<Produto> list) {
        DefaultTableModel model = (DefaultTableModel) tableProduto.getModel();
        Object rowData[] = new Object[5];
        model.setRowCount(0);
        
        for ( Produto produto : list ) {
            rowData[0] = produto.getId();
            rowData[1] = produto.getNome();
            rowData[2] = produto.getQuantidade();
            rowData[3] = produto.getPreco();
            rowData[4] = produto.getAtivo().getValue();

            model.addRow(rowData);
        }
    }

