    private void buttonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditarActionPerformed
        java.awt.EventQueue.invokeLater(new Runnable() {
            public void run() {
                entity = new Produto();
                entity.setId((Integer) tableProduto.getValueAt(tableProduto.getSelectedRow(), 0));

                entity = SpringConfig.context.getBean(ProdutoService.class).find(entity).get(0);

                fieldUpdateId.setText(String.valueOf(entity.getId()));
                fieldUpdateNome.setText(entity.getNome());
                fieldUpdatePreco.setText(entity.getPreco().toString());
                fieldUpdateQuantidade.setText(entity.getQuantidade().toString());
                
                comboUpdateAtivo.removeAllItems();
                
                for ( SimNaoType ativo : SimNaoType.values() ) {
                    comboUpdateAtivo.addItem(ativo.getValue());
                }
                
                comboUpdateAtivo.setSelectedIndex(Arrays.asList(SimNaoType.values()).indexOf(entity.getAtivo()));

                dialogUpdate.setLocationRelativeTo(null);
                dialogUpdate.setVisible(true);
            }
        });
    }//GEN-LAST:event_buttonEditarActionPerformed

